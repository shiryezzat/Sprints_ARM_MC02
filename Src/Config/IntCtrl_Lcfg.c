//Enabling interrupts, we give this function the interrupt and it enables it
 void NVIC_ENx(IntCtrl_InterruptType n)
 {
    if( (n>=0) && (n<=31))
    {
        EN0 |= (1<<(n-0));
    }
    if( (n>=32) && (n<=63))
    {
        EN1 |= (1<<(n-32));
    }
    if( (n>=64) && (n<=95))
    {
        EN2 |= (1<<(n-64));
    }
    if( (n>=96) && (n<=127))
    {
        EN3 |= (1<<(n-96));
    }
    if( (n>=128) && (n<=138))
    {
        EN4 |= (1<<(n-128));
    }
 }
